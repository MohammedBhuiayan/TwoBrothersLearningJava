package com.JavaBasic;

public class Factorial {

	
	
	
	
	
	public static void main(String[] args) {
	
		
		int n = 60;
		
		int FactValue = 1;    // 1*2*3*4
		
		for (int i=1; i<=n; i++) {
			
			FactValue =FactValue*i;
		}

		System.out.println(FactValue);
		
	}
	
	

}
